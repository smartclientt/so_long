#include "so_long.h"

int	is_new_line(char c)
{
	if (c == '\n')
		return (1);
	return (0);
}

int	map_height(char **map)
{
	int	i;

	i = 0;
	if (!map)
		return (0);
	while (map[i])
		i++;
	return (i - 1);
}
